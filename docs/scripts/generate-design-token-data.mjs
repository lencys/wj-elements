import fs from 'fs/promises';
import path from 'path';
import { fileURLToPath } from 'url';

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, '..', '..');

const lightThemePath = path.join(repoRoot, 'packages', 'themes', 'light.css');
const darkThemePath = path.join(repoRoot, 'packages', 'themes', 'dark.css');
const designTokensPagePath = path.join(
  repoRoot,
  'docs',
  'src',
  'components',
  'page',
  'theming',
  'DesignTokensPage',
  'index.tsx'
);
const outputPath = path.join(
  repoRoot,
  'docs',
  'src',
  'components',
  'page',
  'theming',
  'DesignTokensPage',
  'generatedColorTokens.ts'
);

const cssVariablePattern = /(--wje-[\w-]+)\s*:\s*([^;]+);/g;
const pageTokenPattern = /token:\s*'(--wje-[^']+)'/g;
const pageColorScalePattern = /createColorScale\(\s*'[^']+'\s*,\s*'(--wje-color-[^']+)'\s*,/g;

function normalizeCssValue(value) {
  return value.replace(/\s+/g, ' ').trim();
}

function extractCssVariables(cssText) {
  const variables = {};

  for (const match of cssText.matchAll(cssVariablePattern)) {
    variables[match[1]] = normalizeCssValue(match[2]);
  }

  return variables;
}

function extractTokenNames(pageSource) {
  return [...new Set([...pageSource.matchAll(pageTokenPattern)].map((match) => match[1]))];
}

function extractColorScaleConfigs(pageSource) {
  const prefixes = [...new Set([...pageSource.matchAll(pageColorScalePattern)].map((match) => match[1]))];

  return prefixes.map((prefix) => ({
    prefix,
    indexes:
      prefix === '--wje-color-contrast'
        ? Array.from({ length: 12 }, (_, index) => index)
        : Array.from({ length: 11 }, (_, index) => index + 1),
  }));
}

function getRequiredVariable(variables, tokenName, sourcePath) {
  const value = variables[tokenName];

  if (!value) {
    throw new Error(`Missing token "${tokenName}" in ${sourcePath}`);
  }

  return value;
}

function resolveCssValue(value, variables, visited = new Set()) {
  const trimmedValue = value.trim();
  const variableMatch = trimmedValue.match(/^var\((--wje-[\w-]+)(?:\s*,\s*[^)]+)?\)$/);

  if (!variableMatch) {
    return trimmedValue;
  }

  const referencedToken = variableMatch[1];

  if (visited.has(referencedToken)) {
    return trimmedValue;
  }

  const nextValue = variables[referencedToken];

  if (!nextValue) {
    return trimmedValue;
  }

  return resolveCssValue(nextValue, variables, new Set(visited).add(referencedToken));
}

function buildDesignTokenValues(tokenNames, lightVariables, darkVariables) {
  const darkVariablesWithFallback = {
    ...lightVariables,
    ...darkVariables,
  };

  return Object.fromEntries(
    tokenNames.map((tokenName) => {
      const lightValue = getRequiredVariable(lightVariables, tokenName, lightThemePath);
      const darkValue = darkVariables[tokenName] ?? lightValue;

      return [
        tokenName,
        {
          light: lightValue,
          dark: darkValue,
          lightResolved: resolveCssValue(lightValue, lightVariables),
          darkResolved: resolveCssValue(darkValue, darkVariablesWithFallback),
        },
      ];
    })
  );
}

function buildColorScaleValues(scaleConfigs, lightVariables, darkVariables) {
  return Object.fromEntries(
    scaleConfigs.map(({ prefix, indexes }) => [
      prefix,
      {
        light: indexes.map((index) => {
          const token = `${prefix}-${index}`;

          return {
            token,
            value: getRequiredVariable(lightVariables, token, lightThemePath),
          };
        }),
        dark: indexes.map((index) => {
          const token = `${prefix}-${index}`;

          return {
            token,
            value: getRequiredVariable(darkVariables, token, darkThemePath),
          };
        }),
      },
    ])
  );
}

function buildModuleSource(designTokenValues, colorScaleValues) {
  return `/**
 * This file is generated by \`docs/scripts/generate-design-token-data.mjs\`.
 * Do not edit manually.
 */

export const designTokenValues = ${JSON.stringify(designTokenValues, null, 2)} as const;

export const colorScaleValues = ${JSON.stringify(colorScaleValues, null, 2)} as const;
`;
}

async function main() {
  const [lightCssText, darkCssText, designTokensPageSource] = await Promise.all([
    fs.readFile(lightThemePath, 'utf8'),
    fs.readFile(darkThemePath, 'utf8'),
    fs.readFile(designTokensPagePath, 'utf8'),
  ]);

  const lightVariables = extractCssVariables(lightCssText);
  const darkVariables = extractCssVariables(darkCssText);
  const tokenNames = extractTokenNames(designTokensPageSource);
  const scaleConfigs = extractColorScaleConfigs(designTokensPageSource);

  if (!tokenNames.length) {
    throw new Error(`No design tokens found in ${designTokensPagePath}`);
  }

  if (!scaleConfigs.length) {
    throw new Error(`No color scales found in ${designTokensPagePath}`);
  }

  const designTokenValues = buildDesignTokenValues(tokenNames, lightVariables, darkVariables);
  const colorScaleValues = buildColorScaleValues(scaleConfigs, lightVariables, darkVariables);
  const moduleSource = buildModuleSource(designTokenValues, colorScaleValues);

  await fs.writeFile(outputPath, moduleSource);

  console.log(`Generated ${path.relative(repoRoot, outputPath)}`);
}

main().catch((error) => {
  console.error(error);
  process.exitCode = 1;
});
